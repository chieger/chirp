import Foundation
import UIKit

extension UIColor {
  static func twitterBlueColor() -> UIColor {
    let twitterBlueColor = UIColor(red: 64/255, green: 153/255, blue: 255/255, alpha: 1.0)
    return twitterBlueColor
  }
}